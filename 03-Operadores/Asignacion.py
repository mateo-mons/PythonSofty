# Operadores de asignacion 

Edad = 55 
print(Edad)

Edad += 5
Edad = Edad + 5
Edad -= 5
print(Edad)

# Operadores de incremento o decremento

Year = 2020

# Incremento 
Year = Year + 1
Year += 1
print(Year)

# Decremento 
Year = Year - 1
Year -= 1
print(Year)

# Pre Incremento 
Year = 1 + Year
print(Year)

# Pre Decremento
Year = 1 - Year
print(Year)


